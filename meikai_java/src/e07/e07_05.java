package e07;
/*
『こんにちは。』と表示するメソッドhelloを作成せよ。
void hello()
 */
public class e07_05 {
	/**
	 * 『こんにちは。』と表示するメソッド
	 *
	 * @author t.hosoya
	 * @since 20190612
	 *
	 */
	static void hello(){
		// こんにちはと表示する
		System.out.println("こんにちは。");
		// mainメソッドに返却する
		return;
	}
	// mainメソッド
	public static void main(String[] arg){
		// こんにちはと表示する
		hello();
	}

}
